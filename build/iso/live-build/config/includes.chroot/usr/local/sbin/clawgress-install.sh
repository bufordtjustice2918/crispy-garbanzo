#!/usr/bin/env bash
# clawgress-install: live-to-disk installer for the Clawgress appliance.
# Replaces ubiquity. Run from the live environment as root.
#
# Usage: clawgress-install --target-disk /dev/sda [--hostname clawgress] [--apply]
set -euo pipefail

TARGET_DISK=""
HOSTNAME_VAL="clawgress"
APPLY=false
MOUNT_ROOT="/mnt/clawgress-install"

usage() {
  echo "usage: clawgress-install --target-disk <disk> [--hostname <name>] [--apply]" >&2
  echo "  --target-disk  Block device to install to (example: /dev/sda)" >&2
  echo "  --hostname     Target hostname (default: clawgress)" >&2
  echo "  --apply        Execute install; omit to dry-run and print plan only" >&2
  exit 1
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --target-disk) TARGET_DISK="$2"; shift 2 ;;
    --hostname)    HOSTNAME_VAL="$2"; shift 2 ;;
    --apply)       APPLY=true; shift ;;
    *) usage ;;
  esac
done

[[ -z "${TARGET_DISK}" ]] && usage

if [[ "$(id -u)" -ne 0 ]]; then
  echo "error: clawgress-install must run as root" >&2
  exit 1
fi

if [[ ! -b "${TARGET_DISK}" ]]; then
  echo "error: ${TARGET_DISK} is not a block device" >&2
  exit 1
fi

EFI_PART="${TARGET_DISK}1"
ROOT_PART="${TARGET_DISK}2"

echo "=== Clawgress Disk Installer ==="
echo "  Target disk : ${TARGET_DISK}"
echo "  EFI part    : ${EFI_PART}"
echo "  Root part   : ${ROOT_PART}"
echo "  Hostname    : ${HOSTNAME_VAL}"
echo "  Apply       : ${APPLY}"
echo ""

if [[ "${APPLY}" = false ]]; then
  echo "Dry-run complete. Pass --apply to execute."
  exit 0
fi

echo "[1/7] Partitioning ${TARGET_DISK} (GPT: 512M EFI + remainder root)..."
sgdisk --zap-all "${TARGET_DISK}"
sgdisk --new=1:0:+512M --typecode=1:EF00 --change-name=1:"EFI"  "${TARGET_DISK}"
sgdisk --new=2:0:0     --typecode=2:8300 --change-name=2:"root" "${TARGET_DISK}"
partprobe "${TARGET_DISK}"
sleep 1

echo "[2/7] Formatting partitions..."
mkfs.vfat -F32 -n EFI  "${EFI_PART}"
mkfs.ext4 -L root -F   "${ROOT_PART}"

echo "[3/7] Mounting target..."
mkdir -p "${MOUNT_ROOT}"
mount "${ROOT_PART}" "${MOUNT_ROOT}"
mkdir -p "${MOUNT_ROOT}/boot/efi"
mount "${EFI_PART}" "${MOUNT_ROOT}/boot/efi"

echo "[4/7] Copying live filesystem to target (excluding live-only paths)..."
rsync -aAX --delete \
  --exclude=/proc \
  --exclude=/sys \
  --exclude=/dev \
  --exclude=/run \
  --exclude=/tmp \
  --exclude=/media \
  --exclude=/mnt \
  --exclude=/lost+found \
  --exclude=/cdrom \
  --exclude=/lib/live \
  --exclude=/etc/live \
  / "${MOUNT_ROOT}/"

# Recreate excluded bind-mount directories.
for d in proc sys dev run tmp media; do
  mkdir -p "${MOUNT_ROOT}/${d}"
done
chmod 1777 "${MOUNT_ROOT}/tmp"

echo "[5/7] Writing /etc/fstab..."
ROOT_UUID="$(blkid -s UUID -o value "${ROOT_PART}")"
EFI_UUID="$(blkid -s UUID -o value "${EFI_PART}")"
cat > "${MOUNT_ROOT}/etc/fstab" <<FSTAB
# /etc/fstab -- generated by clawgress-install
UUID=${ROOT_UUID}  /          ext4  defaults,errors=remount-ro  0 1
UUID=${EFI_UUID}   /boot/efi  vfat  umask=0077                  0 1
tmpfs              /tmp       tmpfs defaults,nosuid,nodev        0 0
FSTAB

echo "[6/7] Setting hostname to ${HOSTNAME_VAL}..."
echo "${HOSTNAME_VAL}" > "${MOUNT_ROOT}/etc/hostname"
sed -i "s/127.0.1.1.*/127.0.1.1\t${HOSTNAME_VAL}/" "${MOUNT_ROOT}/etc/hosts" 2>/dev/null || \
  echo "127.0.1.1	${HOSTNAME_VAL}" >> "${MOUNT_ROOT}/etc/hosts"

echo "[7/7] Installing GRUB bootloader..."
for fs in dev dev/pts proc sys run; do
  mount --bind "/${fs}" "${MOUNT_ROOT}/${fs}"
done
chroot "${MOUNT_ROOT}" grub-install --target=x86_64-efi --efi-directory=/boot/efi \
  --bootloader-id=clawgress --recheck "${TARGET_DISK}" 2>/dev/null || \
chroot "${MOUNT_ROOT}" grub-install --target=i386-pc "${TARGET_DISK}"
chroot "${MOUNT_ROOT}" update-grub
for fs in run sys proc dev/pts dev; do
  umount "${MOUNT_ROOT}/${fs}" || true
done

echo ""
echo "Install complete. Unmounting..."
umount "${MOUNT_ROOT}/boot/efi"
umount "${MOUNT_ROOT}"

echo ""
echo "Clawgress installed to ${TARGET_DISK}. Remove the live media and reboot."
